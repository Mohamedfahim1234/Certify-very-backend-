import mongoose from "mongoose";

const officerSchema = new mongoose.Schema({
    username: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    },
    department: {
        type: String,
        required: true
    }
});

const Officer = mongoose.model("Officer", officerSchema);

export default Officer;